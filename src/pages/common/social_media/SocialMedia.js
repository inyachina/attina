import data from '../../../dataMainPage.json'

export const SocialMedia = ({color, media, bg}) => {
    const defaultColor = "#F8EFFF"
    const socials = [
        {
            name: "inst",
            link: data.Links.inst,
        }, {
            name: "twitter",
            link: data.Links.twitter,
        }, {
            name: "tiktok",
            link: data.Links.tiktok,
        }, {
            name: "youtube",
            link: data.Links.youtube,
        }, {
            name: "metamask",
            link: data.Links.youtube,
        },
    ]
    const social = useRef(socials.find((s) => s.name === media))

    const renderSwitch = (param, fill) => {
        switch (param) {
            case 'inst':
                return <InstIcon fill={fill}/>;
            case 'twitter':
                return <TwitterIcon fill={fill}/>;
            case 'tiktok':
                return <TiktokIcon fill={fill}/>;
            case 'youtube':
                return <YoutubeIcon fill={fill}/>;
            case 'metamask':
                return <MetamaskIcon fill={fill}/>;
        }
    }
    return <a
        href={social.current.link}
        style={bg && {background: bg}}
        className={`social_media ${(social.current.name === "metamask") && 'social_media_large'}`}>
        {renderSwitch(social.current.name, (color ? color : defaultColor))}
    </a>
}

